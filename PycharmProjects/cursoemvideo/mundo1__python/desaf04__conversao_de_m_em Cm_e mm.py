# «««« Conversão de metros em centimetros e milimetros »»»»»
m = float(input("Diga-me uma valor qualquer em metros:"))
km = m / 1000
hm = m / 100
dam = m / 10
dm = m * 10
cm = m * 100
mm = m * 1000
print("{} metros equivale à: ".format(m))
print("{}km".format(km))
print("{}hm".format(hm))
print("{}dam".format(dam))
print("{}dm".format(dm))
print("{}cm".format(cm))
print("{}mm".format(mm))


