""" Fatiamento de Strings
012345678
olá mundo
-987654321
Fatiamento [i:f:p] [::]
obs: A função len retorna a qtd de caracteres de str"""
variavel = 'Olá mundo'
print(variavel[::-1]) # vai inveter
print(variavel[-1:-10:-1]) # vai inverter
print(variavel[:]) # printar tudo
print(variavel[0:9:2]) # vai até o indice 9 pulando de 2 em 2 casas

